struct SessionAdapter(Arc<dyn StoragePort>);

#[async_trait]
impl SessionPort for SessionAdapter {
    async fn create(&self, session: Session) -> rusvel_core::error::Result<SessionId> {
        let id = session.id;
        self.0.sessions().put_session(&session).await?;
        Ok(id)
    }
    async fn load(&self, id: &SessionId) -> rusvel_core::error::Result<Session> {
        self.0.sessions().get_session(id).await?.ok_or_else(|| {
            rusvel_core::error::RusvelError::NotFound {
                kind: "session".into(),
                id: id.to_string(),
            }
        })
    }
    async fn save(&self, session: &Session) -> rusvel_core::error::Result<()> {
        self.0.sessions().put_session(session).await
    }
    async fn list(&self) -> rusvel_core::error::Result<Vec<SessionSummary>> {
        self.0.sessions().list_sessions().await
    }
}

fn build_harvest_config(
    config: &TomlConfig,
    profile_path: &std::path::Path,
) -> harvest_engine::HarvestConfig {
    let mut skills: Vec<String> = Vec::new();
    if let Ok(Some(v)) = config.get_value("harvest.skills") {
        if let Some(arr) = v.as_array() {
            skills.extend(
                arr.iter()
                    .filter_map(|x| x.as_str().map(str::trim))
                    .filter(|s| !s.is_empty())
                    .map(String::from),
            );
        } else if let Some(s) = v.as_str() {
            skills.extend(
                s.split(',')
                    .map(str::trim)
                    .filter(|t| !t.is_empty())
                    .map(String::from),
            );
        }
    }
    if skills.is_empty() {
        if let Ok(p) = UserProfile::load(profile_path) {
            skills.extend(p.skills.primary);
            skills.extend(p.skills.secondary);
        }
    }
    if skills.is_empty() {
        skills.push("rust".into());
    }
    harvest_engine::HarvestConfig {
        skills,
        min_budget: None,
    }
}

