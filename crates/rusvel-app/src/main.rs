/// Object store bucket for [`EventTrigger`] definitions (CRUD can be added via API later).
const EVENT_TRIGGERS_STORE: &str = "event_triggers";

async fn load_event_triggers_from_store(
    tm: &TriggerManager,
    storage: &Arc<dyn StoragePort>,
) -> Result<()> {
    let rows = storage
        .objects()
        .list(EVENT_TRIGGERS_STORE, ObjectFilter::default())
        .await
        .map_err(|e| anyhow::anyhow!("{e}"))?;
    let mut n = 0usize;
    for v in rows {
        if let Ok(t) = serde_json::from_value::<EventTrigger>(v) {
            tm.register_trigger(t);
            n += 1;
        }
    }
    if n > 0 {
        tracing::info!("Loaded {n} event trigger(s) from object store");
    } else {
        tracing::debug!("No event triggers in object store ({EVENT_TRIGGERS_STORE})");
    }
    Ok(())
}

